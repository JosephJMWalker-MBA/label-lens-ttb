#!/usr/bin/env node
/**
 * Deterministic generation of the low-resolution M Cellars degraded derivative.
 *
 * Parent: the committed, privacy-screened M Cellars OCR-benchmark JPEG
 *   tests/fixtures/precheck/m-cellars-24205001000905/label-ocr-source.jpeg
 *   (sha256 0b0ccec13bf6c533ec7928b017b140a0213fb4555812fea81d71872adb453713)
 *
 * Transformation: a single fixed-width downscale (cubic kernel) re-encoded as
 * PNG (compressionLevel 9). No cropping, no manual pixel or text correction, no
 * added noise. The transform is fully specified by the constants below, so the
 * output is reproducible from the committed parent on the same libvips build.
 *
 * Byte-for-byte determinism across environments is NOT guaranteed: libvips /
 * PNG-encoder versions can differ. Determinism WAS verified on the development
 * environment (sharp ^0.35.3 / this repo's lockfile). The committed asset's
 * exact identity — recorded in the manifest and the corpus index — is the source
 * of truth for tests; this script documents how it was produced, not a promise
 * that every machine reproduces identical bytes.
 *
 * Usage:
 *   node scripts/fixtures/generate-lowres-derivative.mjs          # write asset
 *   node scripts/fixtures/generate-lowres-derivative.mjs --check  # verify only
 */
import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import sharp from "sharp";

const REPO = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
const PARENT = join(REPO, "tests/fixtures/precheck/m-cellars-24205001000905/label-ocr-source.jpeg");
const PARENT_SHA = "0b0ccec13bf6c533ec7928b017b140a0213fb4555812fea81d71872adb453713";
const OUT = join(REPO, "tests/fixtures/precheck/m-cellars-lowres-24205001000905/label-lowres.png");

// The complete, fixed transformation parameters.
export const TRANSFORM = { targetWidth: 601, kernel: "cubic", pngCompressionLevel: 9 };

function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

async function generate() {
  const parentBytes = readFileSync(PARENT);
  if (sha256(parentBytes) !== PARENT_SHA) {
    throw new Error("parent OCR-benchmark JPEG hash does not match the recorded provenance.");
  }
  const bytes = await sharp(parentBytes)
    .resize({ width: TRANSFORM.targetWidth, kernel: TRANSFORM.kernel })
    .png({ compressionLevel: TRANSFORM.pngCompressionLevel })
    .toBuffer();
  const meta = await sharp(bytes).metadata();
  return {
    bytes,
    width: meta.width,
    height: meta.height,
    sha256: sha256(bytes),
    byteSize: bytes.length,
  };
}

const check = process.argv.includes("--check");
const result = await generate();
if (check) {
  const existing = readFileSync(OUT);
  const same = Buffer.compare(existing, result.bytes) === 0;
  console.log(
    same
      ? `OK: regenerated bytes match committed asset (${result.width}x${result.height}, ${result.byteSize} bytes, sha ${result.sha256}).`
      : `DIFFERS: committed sha ${sha256(existing)} vs regenerated ${result.sha256}.`,
  );
  process.exit(same ? 0 : 1);
} else {
  writeFileSync(OUT, result.bytes);
  console.log(
    `wrote ${OUT}\n  ${result.width}x${result.height}, ${result.byteSize} bytes, sha256 ${result.sha256}`,
  );
}
