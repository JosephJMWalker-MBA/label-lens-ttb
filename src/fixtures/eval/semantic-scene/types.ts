import type {
  AnalyzerCandidateProvenance,
  AnalyzerCandidateRanking,
  EvidenceGeometry,
} from "@/pipeline/analyzer/analyzer.types";
import type { BrandLineReason, AlcoholRejectionReason } from "@/pipeline/extractor/field-selection";
import type { OcrPassKind, OcrPassTriggerReason } from "@/pipeline/extractor/extractor.types";

import type { EvalAnnotationConfidence, EvalFailureClass } from "../eval-manifest.types";
import type { SemanticRegionClass, SemanticRegionFamily } from "./ontology";

export type SemanticActiveField = "brand" | "alcohol";

export type SemanticRegionState =
  "proposed" | "provisionally_classified" | "partially_read" | "resolved" | "unresolved";

export type SemanticRegionRelationshipType =
  | "contains"
  | "same_panel_as"
  | "adjacent_to"
  | "aligned_with"
  | "continuation_of"
  | "conflicts_with"
  | "alternative_to"
  | "supports_field"
  | "contextualizes";

export type SemanticProposalSource =
  | "annotated_target"
  | "ocr_pass_region"
  | "ocr_token_or_line"
  | "brand_line"
  | "brand_candidate"
  | "alcohol_window_or_candidate"
  | "recovery_crop"
  | "panel_annotation"
  | "unknown_source";

export type SemanticAcquisitionOperation =
  | "generic_text_ocr"
  | "stylized_text_ocr"
  | "numeric_mandatory_statement_ocr"
  | "barcode_decoder_future"
  | "no_read_contextual"
  | "unresolved_operation";

export type SemanticOperationAgreement = "agree" | "disagree" | "unresolved";

export type SemanticObservedBasisKind =
  | "annotation"
  | "filter_reason"
  | "text_shape"
  | "regulatory_marker"
  | "candidate_status"
  | "proposal_source"
  | "geometry"
  | "pass_eligibility"
  | "unknown";

export interface SemanticObservedBasis {
  kind: SemanticObservedBasisKind;
  value: string;
  sourceRef: string;
}

export interface SemanticClassHypothesis {
  semanticClass: SemanticRegionClass;
  family: SemanticRegionFamily;
  /** Deterministic ordering feature only; never a probability or correctness confidence. */
  rankingScore: number;
  observedBasis: SemanticObservedBasis[];
}

export interface SemanticContentToken {
  text: string;
  rawOcrScore: number | null;
  processedBox: { x0: number; y0: number; x1: number; y1: number };
  originalGeometry: EvidenceGeometry;
}

export interface SemanticContentObservation {
  id: string;
  rawText: string;
  tokens: SemanticContentToken[];
  passId: string;
  passKind: OcrPassKind;
  regionName: string;
  preprocessing: string[];
  triggerReasons: OcrPassTriggerReason[];
  candidateProvenance?: AnalyzerCandidateProvenance;
}

export type SemanticCandidateStatus =
  "selected" | "alternate" | "quarantined" | "filtered" | "retained" | "not_projected";

export interface SemanticProjectionCandidate {
  id: string;
  field: SemanticActiveField;
  /** Exact OCR text from the existing artifact. */
  observedText: string;
  /** Existing production normalization, never generated by the semantic diagnostic. */
  productionValue: string | null;
  status: SemanticCandidateStatus;
  productionDecision: string | null;
  filterReason: BrandLineReason | AlcoholRejectionReason | null;
  ocrEvidenceScore: number;
  parsedPercent: number | null;
  ranking?: AnalyzerCandidateRanking;
}

export interface SemanticAcquisitionRecord {
  actualOperation: SemanticAcquisitionOperation;
  recommendedOperation: SemanticAcquisitionOperation;
  passId: string | null;
  usefulContentObserved: boolean;
  note: string;
}

export interface SemanticRegionRelationship {
  type: SemanticRegionRelationshipType;
  targetNodeId: string;
  observedBasis: string;
}

export interface SemanticRegionNode {
  id: string;
  caseId: string;
  geometry: EvidenceGeometry;
  parentPanelId: string | null;
  proposalSource: SemanticProposalSource;
  proposalBasis: string;
  evaluationRole: "system_proposal" | "annotation_anchor";
  visualObservations: string[];
  classHypotheses: SemanticClassHypothesis[];
  contentObservations: SemanticContentObservation[];
  acquisitionHistory: SemanticAcquisitionRecord[];
  relationships: SemanticRegionRelationship[];
  projectionCandidates: SemanticProjectionCandidate[];
  state: SemanticRegionState;
}

export interface SemanticNormalizedBox {
  x: number;
  y: number;
  width: number;
  height: number;
}

export interface SemanticPanelAnnotation {
  id: string;
  geometry: SemanticNormalizedBox;
  semanticClass: "display_panel" | "information_panel" | "auxiliary_panel" | "unknown_panel";
  annotationConfidence: EvalAnnotationConfidence;
  adjudicationBasis: string;
}

export interface SemanticObjectAnnotation {
  id: string;
  caseId: string;
  geometry: SemanticNormalizedBox;
  semanticClasses: SemanticRegionClass[];
  expectedClass: SemanticRegionClass;
  role: "target" | "hard_negative" | "context";
  relevantField: SemanticActiveField | null;
  annotationConfidence: EvalAnnotationConfidence;
  adjudicationBasis: string;
  legitimateAmbiguity: boolean;
  panelId: string | null;
  expectedOperation: SemanticAcquisitionOperation;
}

export interface SemanticCaseAnnotation {
  caseId: string;
  panels: SemanticPanelAnnotation[];
  objects: SemanticObjectAnnotation[];
}

export type SemanticTerminalCategory =
  | "target_not_proposed"
  | "target_proposed_but_semantically_suppressed"
  | "target_class_preserved_wrong_operation"
  | "content_not_recovered"
  | "object_assembly_failure"
  | "field_projection_failure"
  | "candidate_filtered"
  | "candidate_ranked_below_useful_range"
  | "honest_alternate"
  | "honest_unresolved"
  | "trustworthy_selected_evidence"
  | "false_certainty"
  | "unattributed";

export interface SemanticSurvivalTrace {
  traceId: string;
  caseId: string;
  targetAnnotationId: string;
  field: SemanticActiveField;
  expectedClass: SemanticRegionClass;
  targetProposed: boolean;
  proposalNodeIds: string[];
  proposalSources: SemanticProposalSource[];
  matchedProposalCount: number;
  correctClassTop1: boolean;
  correctClassTop3: boolean;
  strictProposalNodeId: string | null;
  strictTargetProposed: boolean;
  strictCorrectClassTop1: boolean;
  strictCorrectClassTop3: boolean;
  retainedAlternatives: SemanticRegionClass[];
  targetIncorrectlySuppressed: boolean;
  representativeNodeId: string | null;
  representativeRecommendedOperation: SemanticAcquisitionOperation;
  contentBearingNodeId: string | null;
  contentBearingNodeIds: string[];
  contentBearingRecommendedOperation: SemanticAcquisitionOperation;
  contentBearingRecommendedOperations: SemanticAcquisitionOperation[];
  contentBearingOperationRecommendations: Array<{
    nodeId: string;
    recommendedOperation: SemanticAcquisitionOperation;
  }>;
  actualAcquisitionOperation: SemanticAcquisitionOperation;
  actualAcquisitionOperations: SemanticAcquisitionOperation[];
  expectedEvaluationOperation: SemanticAcquisitionOperation;
  representativeOperationAgreement: SemanticOperationAgreement;
  contentBearingOperationAgreement: SemanticOperationAgreement;
  actualOperationAgreement: SemanticOperationAgreement;
  operationFailureCausallySupported: boolean;
  operationDiagnosticBasis: string[];
  contentRecovered: boolean;
  sceneObjectAssembled: boolean;
  fieldCandidateProjected: boolean;
  candidateStatus: SemanticCandidateStatus;
  trustworthyDownstreamEvidence: boolean;
  falseCertainty: boolean;
  tokenFirstFailureClass: EvalFailureClass;
  terminalCategory: SemanticTerminalCategory;
  attributionBasis: string[];
}

export interface SemanticCaseDiagnostic {
  ontologyVersion: string;
  annotationSchemaVersion: string;
  caseId: string;
  annotation: SemanticCaseAnnotation;
  nodes: SemanticRegionNode[];
  traces: SemanticSurvivalTrace[];
  omittedProposalCount: number;
  limitations: string[];
}
