import type { DeclaredFact } from "@/domain/run/declared-facts.types";
import type {
  ProductRevisionReference,
  SanitizedDerivativeReference,
  SourceArtifactReference,
} from "@/domain/run/analysis-run.types";
import type { RuleVersionRef, VersionManifest } from "@/domain/run/version-manifest.types";
import type { VerificationFinding } from "@/domain/verification/finding.types";
import type {
  AnalyzerFieldObservation,
  AnalyzerProvenance,
} from "@/pipeline/analyzer/analyzer.types";
import type {
  EvidenceAssessment,
  PrecheckAdvisoryQuality,
} from "@/pipeline/precheck/precheck.types";

/**
 * The internal, advisory pre-check result contract.
 *
 * This is a structured internal review result — not a COLA approval, a legal
 * determination, or a TTB disposition. Machine findings are preserved exactly
 * and separately from any human disposition, and there is deliberately no
 * overall compliance status, percentage, or aggregate score.
 */
export const RESULT_SCHEMA_VERSION = "precheck-result.v1" as const;
export const RESULT_MODE = "wine-precheck" as const;

/** Fixed, versioned advisory language. Never generated by an LLM. */
export interface AdvisoryNotice {
  noticeId: string;
  noticeVersion: string;
  text: string;
}

/** Immutable run references copied into the result. */
export interface ResultRunReference {
  runId: string;
  /** Creation timestamp supplied by the immutable run; never generated here. */
  createdAt: string;
  product: ProductRevisionReference;
  sourceArtifact: SourceArtifactReference;
  sanitizedDerivative: SanitizedDerivativeReference;
}

/** The two bounded field observations, with their shared analyzer provenance. */
export interface ResultObservations {
  provenance: AnalyzerProvenance;
  brandName: AnalyzerFieldObservation;
  alcoholStatement: AnalyzerFieldObservation;
}

export interface ResultProfile {
  id: string;
  version: string;
  ruleManifest: RuleVersionRef[];
}

/**
 * The complete assembled result. `machineResultId` identifies the immutable
 * machine result and is derived without the human disposition history, so
 * appending disposition never changes it. The history is append-only.
 */
export interface PrecheckResult {
  machineResultId: string;
  resultSchemaVersion: typeof RESULT_SCHEMA_VERSION;
  mode: typeof RESULT_MODE;
  profile: ResultProfile;
  run: ResultRunReference;
  declaredFacts: {
    applicationBrandName: DeclaredFact;
    applicationAlcoholValue: DeclaredFact;
  };
  evidenceAssessments: EvidenceAssessment[];
  observations: ResultObservations;
  findings: VerificationFinding[];
  versionManifest: VersionManifest;
  advisoryNotice: AdvisoryNotice;
  /** Advisory-only quality metadata; never alters an evidence assessment. */
  advisoryQuality?: PrecheckAdvisoryQuality;
  /** Append-only human disposition, kept separate from machine findings. */
  humanDispositionHistory: DispositionEntry[];
}

// ---------------------------------------------------------------------------
// Human disposition — internal workflow only, never government-authority language.
// ---------------------------------------------------------------------------

export const RESULT_DISPOSITION_DECISIONS = [
  "accepted_for_internal_use",
  "correction_requested",
  "additional_evidence_requested",
  "escalated_for_human_review",
  "superseded",
  "no_action",
] as const;
export type ResultDispositionDecision = (typeof RESULT_DISPOSITION_DECISIONS)[number];

/** References from a disposition to the machine findings it concerns (never mutates them). */
export interface DispositionReferences {
  ruleIds?: string[];
  checkIds?: string[];
}

export interface DispositionEntry {
  dispositionId: string;
  /** Contiguous, monotonic from 1. Assigned on append. */
  sequence: number;
  actorId: string;
  /** Supplied by the caller; never generated here. */
  recordedAt: string;
  decision: ResultDispositionDecision;
  reasonCode: string;
  note?: string;
  references?: DispositionReferences;
}

/** Caller-supplied fields for a new disposition; sequence is assigned on append. */
export interface DispositionEntryInput {
  dispositionId: string;
  actorId: string;
  recordedAt: string;
  decision: ResultDispositionDecision;
  reasonCode: string;
  note?: string;
  references?: DispositionReferences;
}

export type AssemblyErrorCode =
  | "RUN_PROFILE_MISMATCH"
  | "RULE_MANIFEST_MISMATCH"
  | "ARTIFACT_IDENTITY_MISMATCH"
  | "DECLARED_FACT_MISMATCH"
  | "UNRESOLVED_EVIDENCE_REFERENCE"
  | "INVALID_FINDING"
  | "INVALID_RESULT_SHAPE"
  // Cross-layer executable-provenance reconciliation failures. Each identifies
  // the exact mismatched identity rather than silently repairing it.
  | "EXTRACTION_ADAPTER_VERSION_MISMATCH"
  | "OCR_ENGINE_VERSION_MISMATCH"
  | "OCR_MODEL_IDENTITY_MISMATCH"
  | "PARSER_VERSION_MISMATCH"
  | "PROFILE_VERSION_MISMATCH"
  | "RULE_VERSION_MISMATCH"
  | "AUTHORITY_VERSION_MISMATCH"
  | "APPLICATION_BUILD_IDENTITY_MISMATCH"
  | "SOURCE_ARTIFACT_IDENTITY_MISMATCH"
  | "DERIVATIVE_ARTIFACT_IDENTITY_MISMATCH";

export interface AssemblyError {
  code: AssemblyErrorCode;
  message: string;
  issues: string[];
}

export type DispositionErrorCode = "DUPLICATE_DISPOSITION_ID" | "INVALID_DISPOSITION";

export interface DispositionError {
  code: DispositionErrorCode;
  message: string;
  issues: string[];
}
